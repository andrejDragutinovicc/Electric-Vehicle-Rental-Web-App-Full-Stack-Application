package com.mycompany.mavenprojectpokusaj2.dto.impl;

import com.mycompany.mavenprojectpokusaj2.dto.Dto;
import com.mycompany.mavenprojectpokusaj2.entity.impl.Role;

/**
 *
 * @author andre
 */
public class KorisnikDto implements Dto{
    
    private Long id;
    private String username;
    private String email;
    private Role role;

    public KorisnikDto(Long id, String username, String email, Role role) {
        this.id = id;
        this.username = username;
        this.email = email;
        this.role = role;
    }

    public Long getId() {
        return id;
    }

    public String getUsername() {
        return username;
    }

    public String getEmail() {
        return email;
    }

    public Role getRole() {
        return role;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public void setRole(Role role) {
        this.role = role;
    }
    
    
}
